#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmp;
	int	tmp1;

	tmp = *a / *b;
	tmp1 = *a % *b;
	*a = tmp;
	*b = tmp1;
}
